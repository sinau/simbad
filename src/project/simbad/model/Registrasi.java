package project.simbad.model;

public class Registrasi {
	private String idRegistrasi;
	private String nama;
	private String alamat;
	private String asalSekolah;
	private String noTelepon;
	private String tanggalPembuatan;
	private String tanggalUpdate;

	public String getIdRegistrasi() {
		return idRegistrasi;
	}

	public void setIdRegistrasi(String idRegistrasi) {
		this.idRegistrasi = idRegistrasi;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getAlamat() {
		return alamat;
	}

	public void setAlamat(String alamat) {
		this.alamat = alamat;
	}

	public String getAsalSekolah() {
		return asalSekolah;
	}

	public void setAsalSekolah(String asalSekolah) {
		this.asalSekolah = asalSekolah;
	}

	public String getNoTelepon() {
		return noTelepon;
	}

	public void setNoTelepon(String noTelepon) {
		this.noTelepon = noTelepon;
	}

	public String getTanggalPembuatan() {
		return tanggalPembuatan;
	}

	public void setTanggalPembuatan(String tanggalPembuatan) {
		this.tanggalPembuatan = tanggalPembuatan;
	}

	public String getTanggalUpdate() {
		return tanggalUpdate;
	}

	public void setTanggalUpdate(String tanggalUpdate) {
		this.tanggalUpdate = tanggalUpdate;
	}

}
